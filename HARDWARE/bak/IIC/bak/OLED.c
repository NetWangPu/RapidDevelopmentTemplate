/**
  * @brief  OLED初始化
  * @param  无
  * @retval 无
  */
void OLED_Init(void)
{
	
	OLED_I2C_Init();			//端口初始化
	
	LCD_WrCmd(0xAE);	//关闭显示
	
	LCD_WrCmd(0xD5);	//设置显示时钟分频比/振荡器频率
	LCD_WrCmd(0x80);
	
	LCD_WrCmd(0xA8);	//设置多路复用率
	LCD_WrCmd(0x3F);
	
	LCD_WrCmd(0xD3);	//设置显示偏移
	LCD_WrCmd(0x00);
	
	LCD_WrCmd(0x40);	//设置显示开始行
	
	LCD_WrCmd(0xA1);	//设置左右方向，0xA1正常 0xA0左右反置
	
	LCD_WrCmd(0xC8);	//设置上下方向，0xC8正常 0xC0上下反置

	LCD_WrCmd(0xDA);	//设置COM引脚硬件配置
	LCD_WrCmd(0x12);
	
	LCD_WrCmd(0x81);	//设置对比度控制
	LCD_WrCmd(0xCF);

	LCD_WrCmd(0xD9);	//设置预充电周期
	LCD_WrCmd(0xF1);

	LCD_WrCmd(0xDB);	//设置VCOMH取消选择级别
	LCD_WrCmd(0x30);

	LCD_WrCmd(0xA4);	//设置整个显示打开/关闭

	LCD_WrCmd(0xA6);	//设置正常/倒转显示

	LCD_WrCmd(0x8D);	//设置充电泵
	LCD_WrCmd(0x14);

	LCD_WrCmd(0xAF);	//开启显示
		
	LCD_Fill(0);				//OLED清屏
	LCD_Set_Pos(0,0);	
}
